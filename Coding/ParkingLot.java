public class ParkingLot{
    private int id;
    private int totalNumber;
    private int visiterNumber;
    private int staffNumber;
    private double hoursFee;
    private String address;
    private String towCompany;
    private String contact;
    private String manager;
    private String phoneNumber;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTotalNumber() {
        return totalNumber;
    }

    public void setTotalNumber(int totalNumber) {
        this.totalNumber = totalNumber;
    }

    public int getVisiterNumber() {
        return visiterNumber;
    }

    public void setVisiterNumber(int visiterNumber) {
        this.visiterNumber = visiterNumber;
    }

    public int getStaffNumber() {
        return staffNumber;
    }

    public void setStaffNumber(int staffNumber) {
        this.staffNumber = staffNumber;
    }

    public double getHoursFee() {
        return hoursFee;
    }

    public void setHoursFee(double hoursFee) {
        this.hoursFee = hoursFee;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTowCompany() {
        return towCompany;
    }

    public void setTowCompany(String towCompany) {
        this.towCompany = towCompany;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getManager() {
        return manager;
    }

    public void setManager(String manager) {
        this.manager = manager;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}